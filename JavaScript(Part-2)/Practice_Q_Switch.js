//Que-Use switch statement to print the day of the week using a number variable 'day' with values 1 to 7.
//1=Monday,2=Tuesday & so on

let day= 1;

switch(day){
    case 1: console.log("Mon");
    break;

     case 2: console.log("Tue");
    break;

     case 3: console.log("Wed");
    break;

     case 4: console.log("Thr");
    break;

     case 5: console.log("Fri");
    break;

     case 6: console.log("Sat");
    break;

     case 7: console.log("Sun");
    break;

    default:  console.log("Wrong day")

}